"""
utils/names.py — нормализация и вариации ФИО.

Публичный API:
    variants(full)      -> Set[str]   — все написания одного ФИО
    norm(s)             -> str        — канонизированная строка для сравнения

Внутренние:
    _split(full)        -> (last, first, mid)
"""

from __future__ import annotations

import re
from typing import Set, Tuple


# ---------------------------------------------------------------------------
# Нормализация
# ---------------------------------------------------------------------------

def norm(s: str) -> str:
    """
    Приводит строку с именем к «каноническому» виду для сравнения:
    убирает лишние пробелы, заменяет точки на пробелы, lowercase.
    """
    return re.sub(r"\s+", " ", s.replace(".", " ").strip().lower())


# Псевдоним для обратной совместимости со старым именем _norm в streamlit_app.py
_norm = norm


# ---------------------------------------------------------------------------
# Разбивка ФИО
# ---------------------------------------------------------------------------

def _split(full: str) -> Tuple[str, str, str]:
    """Разбивает полное ФИО на (фамилия, имя, отчество)."""
    p = full.split()
    p += ["", "", ""]
    return (p[0], p[1] if len(p) > 1 else "", p[2] if len(p) > 2 else "")


# ---------------------------------------------------------------------------
# Варианты написания
# ---------------------------------------------------------------------------

def variants(full: str) -> Set[str]:
    """
    Генерирует множество всех возможных вариантов написания ФИО:
    полное имя, с инициалами с точками, без точек и т.д.

    Пример:
        variants("Иванов Иван Иванович") ->
        {"Иванов Иван Иванович", "Иванов ИИ", "Иванов И.И.", "ИИ Иванов", ...}
    """
    last, first, mid = _split(full.strip())
    fi, mi = first[:1], mid[:1]
    init = fi + mi
    init_dots = ".".join(init) + "." if init else ""
    return {
        v.strip()
        for v in {
            full,
            f"{last} {first} {mid}".strip(),
            f"{last} {init}",
            f"{last} {init_dots}",
            f"{init} {last}",
            f"{init_dots} {last}",
        }
        if v
    }
